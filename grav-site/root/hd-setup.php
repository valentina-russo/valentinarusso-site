    $log[] = 'Setup complete. Deleting self...';
    unlink(__FILE__);
    $log[] = 'Self-deleted OK';
}

// ── Response ──────────────────────────────────────────────────────────────────
echo json_encode([
    'ok'     => empty($errors),
    'log'    => $log,
    'errors' => $errors,
], JSON_PRETTY_PRINT);
