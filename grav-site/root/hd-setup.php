    die(json_encode(['ok' => false, 'error' => 'Forbidden']));
}

$log = [];
$errors = [];

// ── Step 1: generate pepper ───────────────────────────────────────────────────
$pepper = bin2hex(random_bytes(32));
$log[] = 'Pepper generated: ' . substr($pepper, 0, 8) . '...';

// ── Step 2: write hd-db-config.php ───────────────────────────────────────────
$configPath = __DIR__ . '/hd-db-config.php';
$configContent = '<?php' . "\n" .
    '// HD Account System — DB Config (auto-generated by hd-setup.php)' . "\n" .
    "define('HD_DB_HOST',    'localhost');\n" .
    "define('HD_DB_NAME',    'valenti7_hd');\n" .
    "define('HD_DB_USER',    'valenti7_hduser');\n" .
    "define('HD_DB_PASS',    'y\$z4&qd{Bta%vsub');\n" .
    "define('HD_DB_PORT',    '3306');\n" .
    "define('HD_PEPPER',     '" . $pepper . "');\n" .
    "define('HD_BASE_URL',   'https://valentinarussobg5.com');\n" .
    "define('HD_FROM_EMAIL', 'noreply@valentinarussobg5.com');\n" .
    "define('HD_FROM_NAME',  'Valentina Russo');\n";

